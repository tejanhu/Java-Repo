    private void upPart(){
        String id =partTextField.getText();
        String query = "select count(PartID) from vehicleRepairs where PartID='"+id+"'";
        try{
            
            
            stat =  (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            if (res.next())
            {
                String count=res.getString("count(PartID)");
                widLabel.setText(count);
                
            }
            stat.close();
            res.close();
        }
        
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
        
        
        
        
    }
    private void updateTab(){
        try {
            String query = "select trackID,ordersID,PartID,ThirdPartySuppliersID,availableParts,partAdded,withdrawalParts from Track";
            stat =  (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            trackTable.setModel(DbUtils.resultSetToTableModel(res));
            res.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }
    
