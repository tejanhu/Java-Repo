    private void trackTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_trackTableMouseClicked
        try{
            int row = trackTable.getSelectedRow();
            String tTable_click = (trackTable.getModel().getValueAt(row, 0).toString());
            String query = "select * from Track where trackID ='"+tTable_click+" ' ";
            stat =  (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            if (res.next())
            {
                String tID = res.getString("trackID");
                trackID.setText(tID);
                String oID=res.getString("ordersID");
                ordersTextField.setText(oID);
                String tpsID = res.getString("ThirdPartySuppliersID");
                tpsLabel.setText(tpsID);
                String pID=res.getString("PartID");
                partTextField.setText(pID);
                String aviLabel = res.getString("availableParts");
                avaiLabel.setText(aviLabel);
                String aLabel = res.getString("partAdded");
                addLabel.setText(aLabel);
                String wiLabel = res.getString("withdrawalParts");
                widLabel.setText(wiLabel);
                
            }
            stat.close();
            res.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        updateTab(); //Update the table
        

    }//GEN-LAST:event_trackTableMouseClicked

