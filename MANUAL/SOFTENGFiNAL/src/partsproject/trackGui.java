    private void addTrackButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTrackButtonActionPerformed
        String message = " Are you sure want to track the data?";
        String title = "Track";
        int reply = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);
        if (reply == JOptionPane.YES_OPTION) {
        String query = "insert into Track(trackID,ordersID,PartID,ThirdPartySuppliersID,availableParts,partAdded,withdrawalParts) values (?,?,?,?,?,?,?) ";
        try{
            stat =  (PreparedStatement) connect.prepareStatement(query);
            stat.setString(2,ordersTextField.getText());
            stat.setString(3,partTextField.getText());
            stat.setString(4,tpsLabel.getText());
            stat.setString(5,avaiLabel.getText());
            stat.setString(6,addLabel.getText());
            stat.setString(7,widLabel.getText());
            stat.execute();
            JOptionPane.showMessageDialog(null,"Data Saved Succesfully");
            stat.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
        updateTab();
        clearTrack();}
    }//GEN-LAST:event_addTrackButtonActionPerformed

    private void ordersTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ordersTextFieldKeyReleased
       try{
        String value = ordersTextField.getText();
        int key = Integer.parseInt(value);
        String query = "select * from Orders where ordersID='"+key+"' ";
        try{
            stat = (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            if(res.next())
            {
                String addTPS=res.getString("thirdPartySuppliersID");
                tpsLabel.setText(addTPS);
                String addID=res.getString("PartID");
                partTextField.setText(addID);
                String addQ=res.getString("Quantity");
                addLabel.setText(addQ);
            }
            stat.close();
            res.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
       }
       
       catch(Exception e){
            JOptionPane.showMessageDialog(null, "Enter Order ID");
       }
    }//GEN-LAST:event_ordersTextFieldKeyReleased

