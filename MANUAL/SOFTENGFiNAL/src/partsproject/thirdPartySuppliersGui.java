    private void tpsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tpsTableMouseClicked
        
        try {
            
            int rows = tpsTable.getSelectedRow();
            String table_click = (tpsTable.getModel().getValueAt(rows, 0).toString());
            
            String query = "select * from ThirdPartySuppliers where thirdPartyID='" + table_click + " ' ";
            
            stat = (PreparedStatement) connect.prepareStatement(query);
            
            res = stat.executeQuery();
            
            if (res.next()) {
                
                String addID = res.getString("thirdPartyID");
                tpsLabel.setText(addID);
                String addFn = res.getString("Firstname");
                firstNameTextField.setText(addFn);
                String addLn = res.getString("Lastname");
                lastNameTextField.setText(addLn);
                String addPn = res.getString("Phonenumber");
                phoneTextField.setText(addPn);
                String addAddressID = res.getString("addressID");
                addIDTextField.setText(addAddressID);
            }
            stat.close();
        } catch (Exception ex) {
            
            JOptionPane.showMessageDialog(null, ex);
        }
        

    }//GEN-LAST:event_tpsTableMouseClicked

