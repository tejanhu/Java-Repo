    private void updateTPart(){
        String query = "select TrackID,thirdPartySuppliersID from Track";
        try {
            stat = (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            tPartTable.setModel(DbUtils.resultSetToTableModel(res));
            stat.close();
        } catch (Exception ex) {
             JOptionPane.showMessageDialog(null,ex);
        }
    }
    
