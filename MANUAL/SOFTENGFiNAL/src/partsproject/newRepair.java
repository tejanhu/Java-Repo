    private void repTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_repTableMouseClicked
        try{
            int row = repTable.getSelectedRow();
            String repTable_click = (repTable.getModel().getValueAt(row, 0).toString());
            String query = "select * from vehicleRepairs where RepairsID ='"+repTable_click+" ' ";
            stat =  (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            if (res.next())
            {
                String repID=res.getString("RepairsID");
                rIDTextField.setText(repID);
                String mecID=res.getString("mechanicID");
                mecIDTextField.setText(mecID);
                String addVec=res.getString("vehicleID");
                vecTextField.setText(addVec);
                String addID=res.getString("PartID");
                pIDTextField.setText(addID);
                String addNam=res.getString("partName");
                pNamTextField.setText(addNam);
                String addDat=res.getString("dateInstalled");
                iDateTextField.setText(addDat);
                String addWDate=res.getString("dateWarranty");
                wDateTextField.setText(addWDate);
            }
            stat.close();
            res.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
        updateRepair(); //Update the table
    }//GEN-LAST:event_repTableMouseClicked

