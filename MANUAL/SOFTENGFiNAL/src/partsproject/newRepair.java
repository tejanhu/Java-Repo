    private void wDateTextFieldMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_wDateTextFieldMouseClicked
        String iDate = iDateTextField.getText();
        Date date = new Date();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        dateFormat.applyPattern("dd/MM/yyyy");
        try{
            date=dateFormat.parse(iDate);
            Calendar cal = Calendar.getInstance();
            cal.setTime(date);
            cal.add(Calendar.YEAR, 1);
            date = cal.getTime();
            wDateTextField.setText(dateFormat.format(date));
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Enter the installed date above");
        }
    }//GEN-LAST:event_wDateTextFieldMouseClicked

