    private void checkStock(){
        String value = pIDTextField.getText();
        String query3 = "select * from Parts where PartID='"+value+"' ";
        
        String message = "Do you want to order this part " +value+" from suppliers ?";
        String title = "Part " +value+ " is not in stock";
        try{
            stat = (PreparedStatement) connect.prepareStatement(query3);
            res = stat.executeQuery();
            if(res.next())
            {
                
                String rem = res.getString("partsRemaining");
                if (rem.equalsIgnoreCase("0"))
                {
                    
                    int response = JOptionPane.showConfirmDialog(null, message, title, JOptionPane.YES_NO_OPTION);
                    if (response == JOptionPane.YES_OPTION) {
                        ordersGui order = new ordersGui();
                        order.setVisible(true);
                        setVisible(false);
                        
                    }
                    else{
                        pIDTextField.setText(null);}
                    
                     }
            }
            
            stat.close();
            res.close();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
            
        }
    }
    private void upPart(){
        try {
            String id =pIDTextField.getText();
            String query = "update Parts set partsRemaining = partsRemaining-1 where PartID='"+id+"'";
            stat =  (PreparedStatement) connect.prepareStatement(query);
            stat.execute();
            stat.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }
    
    private void updateRepair(){
        try {
            String query = "select RepairsID,mechanicID,vehicleID,PartID,partName,dateInstalled,dateWarranty from vehicleRepairs";
            stat =  (PreparedStatement) connect.prepareStatement(query);
            res = stat.executeQuery();
            repTable.setModel(DbUtils.resultSetToTableModel(res));
            res.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex);
        }
    }
